import numpy as np

# creating array using array() function
print("Creaing array using asarray() function\n")

# 0D Array
a = np.array(10)
print("0D Array \n", a)
print("\n")

# 1D Array
a = np.array([10,20,30])
print("1D Array \n", a)
print("\n")

# 2D Array
a = np.array([[10,20],[30,40]])
print("2D Array \n", a)
print("\n")

# 3D Array
a = np.array([[[10,20],[30,40]],[[50,60],[70,80]]])
print("3D Array \n", a)
print("\n")


