import numpy as np

# Initialization of array

# zeros()
a = np.zeros(3)
print(a)
print("-"*50)

a = np.zeros([2,3])
print(a)
print("-"*50)

# full()
a = np.full([2,3],5)
print(a)
print("-"*50)

a = np.full([2,3,5],7)
print(a)
print("-"*50)

# random.rand()
a = np.random.rand(2,3)
print(a)
print("-"*50)

# ones()
a = np.ones([3,4])
print(a)
print("-"*50)

# eye()
a = np.eye(3)
print(a)
print("-"*50)

a = np.eye(5)
print(a)
print("-"*50)






