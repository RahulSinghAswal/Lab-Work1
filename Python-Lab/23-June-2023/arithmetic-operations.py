import numpy as np

a = np.array([[10,20,30],[40,50,60]])
b = np.array([[1,2,3],[4,5,6]])

print(a)
print("-"*50)
print(b)

print("Add operation")
c = np.add(a,b)
print(c)
print("-"*50)

print("Subtract operation")
c = np.subtract(a,b)
print(c)
print("-"*50)

print("Multiply operation")
c = np.multiply(a,b)
print(c)
print("-"*50)

print("Divide operation")
c = np.divide(a,b)
print(c)
print("-"*50)

print("Exponent operation")
c = np.exp(a)
print(c)
print("-"*50)

print("Square root operation")
c = np.sqrt(a)
print(c)
print("-"*50)

